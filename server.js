const express = require("express");
const app = express();
const http = require("http");
const path = require("path");
const cors = require("cors");
const { Server } = require("socket.io");
const ACTIONS = require("./src/Actions");

// Create HTTP Server
const server = http.createServer(app);

// Enable CORS for WebSockets
const io = new Server(server, {
    cors: {
        origin: ["https://symphonious-bombolone-67f7b6.netlify.app/"],  // ğŸ”¹ Allow frontend to connect
        methods: ["GET", "POST"],
    }
});

// Apply CORS Middleware
app.use(cors({ origin: "https://symphonious-bombolone-67f7b6.netlify.app/", credentials: true }));

// Serve Static Files
app.use(express.static("build"));

// Serve index.html for valid requests (EXCEPT WebSocket calls)
app.get("*", (req, res) => {
    res.sendFile(path.join(__dirname, "build", "index.html"));
});

// Track Connected Users
const userSocketMap = {};
function getAllConnectedClients(roomId) {
    return Array.from(io.sockets.adapter.rooms.get(roomId) || []).map(
        (socketId) => ({
            socketId,
            username: userSocketMap[socketId],
        })
    );
}

// WebSocket Connection
io.on("connection", (socket) => {
    console.log("âœ… WebSocket Connected:", socket.id);

    socket.on(ACTIONS.JOIN, ({ roomId, username }) => {
        userSocketMap[socket.id] = username;
        socket.join(roomId);

        const clients = getAllConnectedClients(roomId);
        clients.forEach(({ socketId }) => {
            io.to(socketId).emit(ACTIONS.JOINED, {
                clients,
                username,
                socketId: socket.id,
            });
        });
    });

    socket.on(ACTIONS.CODE_CHANGE, ({ roomId, code }) => {
        socket.to(roomId).emit(ACTIONS.CODE_CHANGE, { code });
    });

    socket.on(ACTIONS.SYNC_CODE, ({ socketId, code }) => {
        io.to(socketId).emit(ACTIONS.CODE_CHANGE, { code });
    });

    socket.on("disconnecting", () => {
        const rooms = [...socket.rooms];
        rooms.forEach((roomId) => {
            socket.to(roomId).emit(ACTIONS.DISCONNECTED, {
                socketId: socket.id,
                username: userSocketMap[socket.id],
            });
        });
        delete userSocketMap[socket.id];
    });

    socket.on("disconnect", () => {
        console.log("âŒ WebSocket Disconnected:", socket.id);
    });
});

// Start Server
const PORT = process.env.PORT || 5001;
server.listen(PORT, () => console.log(`ğŸš€ Server running on port ${PORT}`));
